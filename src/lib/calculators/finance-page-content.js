const _ry = (v) => (typeof v === 'string' ? v.replace(/\{\{year\}\}/g, _fy) : v);
function _resolveYearsDeep(obj) {
  if (Array.isArray(obj)) return obj.map(_resolveYearsDeep);
  if (obj && typeof obj === 'object') {
    const out = {};
    for (const k of Object.keys(obj)) out[k] = _resolveYearsDeep(obj[k]);
    return out;
  }
  return _ry(obj);
}

