// One button per Amazon marketplace that actually has an approved tag today
// (only 'fr' right now) — a reader picks the Amazon site they actually use,
// rather than depending on Amazon's own cross-marketplace redirect (OneLink),
// which is unconfirmed for amazon.sa and doesn't cover amazon.ae at all (see
// the note in affiliate-config.js). `amazonQueries.sa`/`.ae` are already
// written in Arabic and ready — they'll start showing buttons the moment
// those tags are filled in, no code changes needed then.
// `mumzworldLinkKey` stays a flat store-wide link since Mumzworld isn't live yet.
function buildStoreOptions({ amazonQueries, mumzworldLinkKey } = {}) {
  const options = [
    ...buildAmazonStoreOptions(amazonQueries || {}),
  ];

  const mumzworldHref = mumzworldLinkKey ? getAffiliateLink(mumzworldLinkKey) : null;
  if (mumzworldHref) options.push({ store: 'Mumzworld', href: mumzworldHref });

  return options;
}

