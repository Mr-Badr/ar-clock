const SITE_URL = getSiteUrl();

function buildFaqSchema({ faqItems, url, pageTitle }) {
  if (!Array.isArray(faqItems) || faqItems.length === 0) return null;

  return {
    '@context': 'https://schema.org',
    '@type': 'FAQPage',
    name: `${pageTitle} | ${SITE_BRAND}`,
    url,
    inLanguage: 'ar',
    mainEntity: faqItems.map((item) => ({
      '@type': 'Question',
      name: item.question,
      acceptedAnswer: {
        '@type': 'Answer',
        text: item.answer,
      },
    })),
  };
}

function createSiteInfoPageDefinition({
  path,
  metadata,
  schemaType = 'WebPage',
  dateModified,
  page,
}) {
  const url = `${SITE_URL}${path}`;
  const { schemaExtra, faqItems, ...pageProps } = page;
  const pageSchema = {
    '@context': 'https://schema.org',
    '@type': schemaType,
    name: `${metadata.title} | ${SITE_BRAND}`,
    url,
    inLanguage: 'ar',
    ...(dateModified ? { dateModified } : {}),
    ...(schemaExtra || {}),
  };
  const faqSchema = buildFaqSchema({
    faqItems,
    url,
    pageTitle: metadata.title,
  });

  return {
    path,
    metadata: buildCanonicalMetadata({
      ...metadata,
      url,
    }),
    page: {
      ...pageProps,
      faqItems,
      schema: faqSchema ? [pageSchema, faqSchema] : pageSchema,
    },
  };
}

