import { SITE_BRAND, SITE_CONTACT_EMAIL, getSiteUrl } from '@/lib/site-config';

const SITE_URL = getSiteUrl();

function createSiteInfoPageDefinition({
  path,
  metadata,
  schemaType = 'WebPage',
  dateModified,
  page,
}) {
  const url = `${SITE_URL}${path}`;
  const { schemaExtra, ...pageProps } = page;

  return {
    path,
    metadata: buildCanonicalMetadata({
      ...metadata,
      url,
    }),
    page: {
      ...pageProps,
      schema: {
        '@context': 'https://schema.org',
        '@type': schemaType,
        name: `${metadata.title} | ${SITE_BRAND}`,
        url,
        inLanguage: 'ar',
        ...(dateModified ? { dateModified } : {}),
        ...(schemaExtra || {}),
      },
    },
  };
}

