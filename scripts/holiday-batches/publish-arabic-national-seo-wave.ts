function buildPackage(config: NationalEventConfig, current: any) {
  const currentDatePublished =
    current?.publishStatus === 'published'
      ? current?.richContent?.seoMeta?.datePublished || NOW_ISO
      : NOW_ISO;

  return {
    ...(current || {}),
    schemaVersion: 1,
    core: {
      id: config.id,
      slug: config.slug,
      name: config.name,
      type: 'fixed',
      category: 'national',
      _countryCode: config.countryCode,
      month: config.month,
      day: config.day,
    },
    richContent: {
      ...(current?.richContent || {}),
      seoTitle: config.seoTitle,
      description: config.description,
      keywords: buildKeywords(config),
      answerSummary: config.answerSummary,
      quickFacts: config.quickFacts,
      aboutEvent: {
        'ما هو هذا اليوم؟': config.aboutEvent['ما هو هذا اليوم؟'],
        'التاريخ والأصل': config.aboutEvent['التاريخ والأصل'],
        'الأهمية والمكانة': config.aboutEvent['الأهمية والمكانة'],
        'كيف يُحيا هذا اليوم': config.aboutEvent['كيف يُحيا هذا اليوم'],
      },
      faq: config.faq,
      faqItems: mirrorFaq(config.faq),
      intentCards: buildIntentCards(),
      engagementContent: config.engagementContent,
      seoMeta: {
        titleTag: config.titleTag,
        metaDescription: config.metaDescription,
        h1: config.h1,
        canonicalPath: `/holidays/${config.slug}`,
        ogTitle: `${config.name} {{year}} | كم باقي؟`,
        ogDescription: config.ogDescription,
        ogImageAlt: `${config.name} {{year}} — {{formattedDate}}`,
        primaryKeyword: config.primaryKeyword,
        secondaryKeywords: config.secondaryKeywords,
        longTailKeywords: config.longTailKeywords,
        datePublished: currentDatePublished,
        dateModified: NOW_ISO,
        inLanguage: 'ar',
        eventCategory: 'national',
      },
      recurringYears: {
        contextParagraph: config.recurringContext,
        sourceNote: config.recurringSourceNote,
        columns: ['السنة', 'التاريخ', 'ملاحظة'],
        highlightCurrentYear: true,
      },
      schemaData: {
        eventName: `${config.name} {{year}}`,
        eventAlternateName: config.englishName || '',
        startDate: `{{year}}-${String(config.month).padStart(2, '0')}-${String(config.day).padStart(2, '0')}`,
        endDate: `{{year}}-${String(config.month).padStart(2, '0')}-${String(config.day).padStart(2, '0')}`,
        eventDescription: config.schemaDescription,
        breadcrumbs: [
          { name: 'الرئيسية', path: '/' },
          { name: 'المناسبات', path: '/holidays' },
          { name: config.name, path: `/holidays/${config.slug}` },
        ],
        articleHeadline: `${config.name} {{year}} — العد التنازلي والمعلومات الكاملة`,
        faqSchemaItems: config.faq,
      },
      relatedSlugs: config.relatedSlugs,
      about: {
        heading: `عن ${config.name}`,
        paragraphs: config.aboutParagraphs,
      },
      history: config.history,
      significance: config.significance,
      details: config.details,
      sources: config.sources,
    },
    countryOverrides: current?.countryOverrides || {},
    aliasSlugs: current?.aliasSlugs || [],
    countryScope: 'none',
    countryAliasTemplate: current?.countryAliasTemplate || '{{slug}}-in-{{countrySlug}}',
    keywordTemplateSet: config.keywordTemplateSet || {
      base: [
        config.primaryKeyword,
        ...config.secondaryKeywords.slice(0, 2),
        ...config.longTailKeywords.slice(0, 2),
      ],
      country: [
        `متى ${config.name} في {{countryName}} {{year}}`,
        `${config.name} {{countryName}} {{year}}`,
      ],
    },
    tier: config.tier,
    publishStatus: 'published',
    canonicalPath: `/holidays/${config.slug}`,
    canonicalSource: 'internal',
    sourceAuthority: config.sourceAuthority,
    queueOrder: config.queueOrder,
  };
}

function buildResearch(config: NationalEventConfig, current: any) {
  return {
    ...(buildEmptyResearchRecord({
      slug: config.slug,
      locale: 'ar',
      capturedAt: NOW_ISO,
    }) as any),
    ...(current || {}),
    slug: config.slug,
    locale: 'ar',
    capturedAt: NOW_ISO,
    primaryQueries: config.primaryQueries,
    competitors: config.competitors,
    coverageMatrix: config.coverageMatrix,
    keywordGaps: config.keywordGaps,
    unansweredQuestions: config.unansweredQuestions,
    differentiationIdeas: config.differentiationIdeas,
    factSources: config.sources,
    notes: config.researchNotes,
  };
}

function buildQa(config: NationalEventConfig, current: any) {
  return {
    ...(buildEmptyQaRecord({
      slug: config.slug,
      tier: config.tier,
      publishStatus: 'published',
      updatedAt: NOW_ISO,
    }) as any),
    ...(current || {}),
    slug: config.slug,
    tier: config.tier,
    publishStatus: 'published',
    checks: {
      contentReady: true,
      factChecked: true,
      schemaValid: true,
      seoValidated: true,
      hasHardcodedYear: false,
    },
    notes: config.sources.map((source) => `fact-source: ${source.label}`),
    updatedAt: NOW_ISO,
  };
}

