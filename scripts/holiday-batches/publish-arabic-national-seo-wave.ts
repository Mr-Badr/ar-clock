const NOW_ISO = '2026-04-10T00:00:00.000Z';

function buildKeywords(config: NationalEventConfig) {
  return Array.from(
    new Set([
      config.primaryKeyword,
      ...config.secondaryKeywords,
      ...config.longTailKeywords,
      ...(config.keywords || []),
    ]),
  );
}

