function qaFor(seed: EventSeed) {
  return {
    slug: seed.slug,
    tier: 'tier1',
    publishStatus: 'published',
    checks: {
      contentReady: true,
      factChecked: true,
      schemaValid: true,
      seoValidated: true,
      hasHardcodedYear: false,
    },
    notes: seed.sources.map((source) => `fact-source: ${source.label}`),
    updatedAt: NOW_ISO,
  };
}

export default defineEventBatch(
  seeds.map((seed) => ({
    slug: seed.slug,
    apply(current) {
      const nextPackage = packageFor(seed);
      return {
        package: {
          ...(current.pkg || {}),
          ...nextPackage,
          richContent: {
            ...((current.pkg && current.pkg.richContent) || {}),
            ...nextPackage.richContent,
          },
        },
        research: researchFor(seed),
        qa: qaFor(seed),
      };
    },
  })),
);
