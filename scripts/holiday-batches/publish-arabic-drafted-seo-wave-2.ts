function buildCoverage(config: BatchConfig): CoverageItem[] {
  return [
    {
      keyword: config.seoMeta.primaryKeyword,
      whyItMatters: 'الطلب المباشر على معرفة الموعد هو المدخل الأول للترتيب.',
    },
    ...config.seoMeta.secondaryKeywords.slice(0, 3).map((keyword) => ({
      keyword,
      whyItMatters: 'يغطي صياغات قريبة يستعملها الباحث العربي قبل المناسبة وأثناءها.',
    })),
  ];
}

function buildPackage(config: BatchConfig, current: any) {
  const currentDatePublished =
    current?.publishStatus === 'published'
      ? current?.richContent?.seoMeta?.datePublished || NOW_ISO
      : NOW_ISO;

  const core = {
    ...(current?.core || {}),
    ...(config.corePatch || {}),
  };

  const name = core.name || current?.core?.name || config.slug;

  return {
    ...(current || {}),
    schemaVersion: 1,
    core,
    richContent: {
      ...(current?.richContent || {}),
      seoTitle: config.seoTitle,
      description: config.description,
      keywords: Array.from(
        new Set([
          ...config.keywords,
          config.seoMeta.primaryKeyword,
          ...config.seoMeta.secondaryKeywords,
          ...config.seoMeta.longTailKeywords,
        ]),
      ),
      answerSummary: config.answerSummary,
      quickFacts: config.quickFacts,
      aboutEvent: config.aboutEvent,
      faq: config.faq,
      faqItems: mirrorFaq(config.faq),
      intentCards: buildIntentCards(config.theme),
      engagementContent: config.engagementContent,
      seoMeta: {
        titleTag: config.seoMeta.titleTag,
        metaDescription: config.seoMeta.metaDescription,
        h1: config.seoMeta.h1,
        canonicalPath: `/holidays/${config.slug}`,
        ogTitle: config.seoMeta.ogTitle,
        ogDescription: config.seoMeta.ogDescription,
        ogImageAlt: config.seoMeta.ogImageAlt,
        primaryKeyword: config.seoMeta.primaryKeyword,
        secondaryKeywords: config.seoMeta.secondaryKeywords,
        longTailKeywords: config.seoMeta.longTailKeywords,
        datePublished: currentDatePublished,
        dateModified: NOW_ISO,
        inLanguage: 'ar',
        eventCategory: core.category,
      },
      recurringYears: {
        contextParagraph: config.recurringYears.contextParagraph,
        sourceNote: config.recurringYears.sourceNote,
        columns: ['السنة', 'التاريخ', 'ملاحظة'],
        highlightCurrentYear: true,
      },
      schemaData: {
        eventName: `${name} {{year}}`,
        eventAlternateName: config.schemaData.eventAlternateName || '',
        startDate: config.schemaData.startDate,
        endDate: config.schemaData.endDate,
        eventDescription: config.schemaData.eventDescription,
        breadcrumbs: [
          { name: 'الرئيسية', path: '/' },
          { name: 'المناسبات', path: '/holidays' },
          { name, path: `/holidays/${config.slug}` },
        ],
        articleHeadline: config.schemaData.articleHeadline,
        faqSchemaItems: config.faq,
      },
      relatedSlugs: config.relatedSlugs,
      about: config.about,
      history: config.history,
      significance: config.significance,
      details: config.details,
      sources: config.sources,
    },
    countryOverrides: current?.countryOverrides || {},
    aliasSlugs: current?.aliasSlugs || [],
    countryScope: current?.countryScope || 'custom',
    countryAliasTemplate: current?.countryAliasTemplate || '{{slug}}-in-{{countrySlug}}',
    keywordTemplateSet: config.keywordTemplateSet || current?.keywordTemplateSet || {},
    tier: config.tier,
    publishStatus: 'published',
    canonicalPath: `/holidays/${config.slug}`,
    canonicalSource: 'internal',
    sourceAuthority: config.sourceAuthority || current?.sourceAuthority || 'editorial',
    queueOrder: current?.queueOrder || 99,
  };
}

function buildResearch(config: BatchConfig, current: any) {
  return {
    ...(buildEmptyResearchRecord({
      slug: config.slug,
      locale: 'ar',
      capturedAt: NOW_ISO,
    }) as any),
    ...(current || {}),
    slug: config.slug,
    locale: 'ar',
    capturedAt: NOW_ISO,
    primaryQueries: config.primaryQueries,
    competitors: current?.competitors || [],
    coverageMatrix: buildCoverage(config),
    keywordGaps: config.seoMeta.longTailKeywords.slice(0, 4),
    unansweredQuestions: [],
    differentiationIdeas: [
      'الجمع بين العد التنازلي والإجابة التفسيرية في صفحة واحدة بدلاً من المحتوى الخبري العابر.',
    ],
    factSources: config.sources,
    notes: config.researchNotes,
  };
}

function buildQa(config: BatchConfig, current: any) {
  return {
    ...(buildEmptyQaRecord({
      slug: config.slug,
      tier: config.tier,
      publishStatus: 'published',
      updatedAt: NOW_ISO,
    }) as any),
    ...(current || {}),
    slug: config.slug,
    tier: config.tier,
    publishStatus: 'published',
    checks: {
      contentReady: true,
      factChecked: true,
      schemaValid: true,
      seoValidated: true,
      hasHardcodedYear: false,
    },
    notes: config.sources.map((source) => `fact-source: ${source.label}`),
    updatedAt: NOW_ISO,
  };
}

function patchRelatedSlugs(current: any, relatedSlugs: string[]) {
  return {
    ...(current || {}),
    richContent: {
      ...(current?.richContent || {}),
      relatedSlugs,
      seoMeta: {
        ...(current?.richContent?.seoMeta || {}),
        dateModified: NOW_ISO,
      },
    },
  };
}

